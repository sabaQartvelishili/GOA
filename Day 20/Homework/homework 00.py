#დავალება 1: მომხმარებელს შემოატანინეთ რიცხვი, შემდეგ კი for ლუპის დახმარებით გავლეთ დიაპაზონი მომხმარებლის შემოტანილ რიცხვამდე.
x = int(input('enter you number :'))
for i in range(0, x) :
    print(i)

#დავალება 2: 10-დან 20-მდე გამოტანეთ ყველა რიცხვი, while ციკლის დახმარებით
i = 9
while i < 19:
    i = i + 1
    print(i)

#დავალება 3: while ციკლის დახმარებით გამოტანეთ 0-დან 100-მდე, მხოლოდ ლუწი რიცხვები
